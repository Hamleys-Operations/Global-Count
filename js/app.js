async function loadData(bustCache) {
  const url = 'data/gc-data.json' + (bustCache ? ('?t=' + Date.now()) : '');
  const res = await fetch(url);
  if (!res.ok) throw new Error('Could not load data/gc-data.json (HTTP ' + res.status + ')');
  const json = await res.json();

  APP.columns = json.columns || [];
  APP.rows = json.rows || [];
  APP.storeMaster = json.storeMaster || [];
  APP.meta = json.meta || {};
  APP.storeMasterByCode = {};
  APP.storeMaster.forEach(m => { APP.storeMasterByCode[m.code] = m; });

  resolveColumnIndexes();
  buildRecords();
  populateFilterOptions();
  applyFilters();

  $('#lastUpdatedText').textContent = APP.meta.lastUpdated || '—';
  $('#storeCountSub').textContent = `${APP.storeMaster.length} Stores Mapped · India Retail Operations`;
  document.getElementById('footerYear').textContent = new Date().getFullYear();
}

function resolveColumnIndexes() {
  const c = APP.columns;
  APP.colIdx = {
    storeCode: findColIndex(c, ['store code']),
    storeName: findColIndex(c, ['store name']),
    rm: findColIndex(c, ['rm']),
    rom: findColIndex(c, ['rom']),
    sd: findColIndex(c, ['sd']),
    date: findColIndex(c, ['date of global count', 'date']),
    gc: findColIndex(c, ["no. of sku's counted", 'no of skus counted', 'sku', 'global count']),
    shortageQty: findColIndex(c, ['shortage qty']),
    shortageMapValue: findColIndex(c, ['shortage map value']),
    excessQty: findColIndex(c, ['excess qty']),
    excessMapValue: findColIndex(c, ['excess map value']),
    netQtyDiff: findColIndex(c, ['total net qty difference', 'net qty difference']),
    netValueDiff: findColIndex(c, ['total net map value difference', 'net map value difference']),
    doneBy: findColIndex(c, ['global count done by', 'done by']),
    validatedBy: findColIndex(c, ['global count validated by', 'validated by']),
    movedTo2997: findColIndex(c, ['shortage qty moved to 2997', 'moved to 2997']),
    reason: findColIndex(c, ['reason for short or excess qty', 'reason']),
  };
}

/** Build normalized record objects from dynamic rows, filling gaps via storeMaster mapping */
function buildRecords() {
  const idx = APP.colIdx;
  APP.records = APP.rows.map((row, i) => {
    const storeCode = idx.storeCode !== -1 ? String(row[idx.storeCode] ?? '').trim() : '';
    const mapping = APP.storeMasterByCode[storeCode];

    let storeName = idx.storeName !== -1 ? row[idx.storeName] : (mapping ? mapping.name : '');
    let rm = idx.rm !== -1 ? row[idx.rm] : '';
    let rom = idx.rom !== -1 ? row[idx.rom] : '';
    let sd = idx.sd !== -1 ? row[idx.sd] : '';

    // Fill missing RM/ROM/SD from master mapping when blank
    if ((!rm || String(rm).trim() === '') && mapping) rm = mapping.rm;
    if ((!rom || String(rom).trim() === '') && mapping) rom = mapping.rom;
    if ((!sd || String(sd).trim() === '') && mapping) sd = mapping.sd;
    if ((!storeName || String(storeName).trim() === '') && mapping) storeName = mapping.name;

    const dateVal = idx.date !== -1 ? parseAnyDate(row[idx.date]) : null;
    const gc = idx.gc !== -1 ? Number(row[idx.gc]) || 0 : 0;

    return {
      _rowIndex: i,
      storeCode,
      storeName: storeName || storeCode || 'Unknown',
      rm: (rm || 'Unassigned').toString().trim(),
      rom: (rom || 'Unassigned').toString().trim(),
      sd: (sd || 'Unassigned').toString().trim(),
      date: dateVal,
      dateStr: dateVal ? toLocalISODate(dateVal) : '',
      month: dateVal ? monthLabel(dateVal) : 'Unknown',
      year: dateVal ? dateVal.getUTCFullYear() : null,
      gc,
      shortageQty: idx.shortageQty !== -1 ? Number(row[idx.shortageQty]) || 0 : 0,
      shortageMapValue: idx.shortageMapValue !== -1 ? Number(row[idx.shortageMapValue]) || 0 : 0,
      excessQty: idx.excessQty !== -1 ? Number(row[idx.excessQty]) || 0 : 0,
      excessMapValue: idx.excessMapValue !== -1 ? Number(row[idx.excessMapValue]) || 0 : 0,
      netQtyDiff: idx.netQtyDiff !== -1 ? Number(row[idx.netQtyDiff]) || 0 : 0,
      netValueDiff: idx.netValueDiff !== -1 ? Number(row[idx.netValueDiff]) || 0 : 0,
      doneBy: idx.doneBy !== -1 ? row[idx.doneBy] : '',
      validatedBy: idx.validatedBy !== -1 ? row[idx.validatedBy] : '',
      movedTo2997: idx.movedTo2997 !== -1 ? row[idx.movedTo2997] : '',
      reason: idx.reason !== -1 ? row[idx.reason] : '',
    };
  });
}

