function renderRaw() {
  const search = ($('#rawSearchInput').value || '').toLowerCase();
  let rows = APP.rows.map((r, i) => ({ r, i }));
  if (search) rows = rows.filter(({ r }) => r.some(cell => String(cell ?? '').toLowerCase().includes(search)));
  if (APP.rawSort.key !== null) {
    rows.sort((a, b) => {
      const va = a.r[APP.rawSort.key], vb = b.r[APP.rawSort.key];
      const dir = APP.rawSort.dir === 'asc' ? 1 : -1;
      if (typeof va === 'number' && typeof vb === 'number') return (va - vb) * dir;
      return String(va ?? '').localeCompare(String(vb ?? '')) * dir;
    });
  }
  APP.rawFilteredRows = rows.map(x => x.r);

  const totalPages = Math.max(1, Math.ceil(rows.length / APP.rawPageSize));
  APP.rawPage = Math.min(APP.rawPage, totalPages);
  const start = (APP.rawPage - 1) * APP.rawPageSize;
  const pageRows = rows.slice(start, start + APP.rawPageSize);

  $('#rawDataTable tbody').innerHTML = pageRows.map(({ r }) => `<tr>${r.map(cell => `<td>${formatRawCell(cell)}</td>`).join('')}</tr>`).join('')
    || `<tr><td colspan="${APP.columns.length}" style="text-align:center;padding:20px;" class="text-muted">No matching rows</td></tr>`;

  renderPagination('#rawDataPagination', APP.rawPage, totalPages, (p) => { APP.rawPage = p; renderRaw(); });
}

function formatRawCell(cell) {
  if (cell === null || cell === undefined || cell === '') return '';
  return String(cell);
}

